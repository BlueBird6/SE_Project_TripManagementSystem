package BussinessLogic;


public class SearchResult {
	
    String tripAct;

    String  tripBudget;

    String  tripChks;

    String tripDate;

    String tripDestination;

    String  tripID;

    String tripOrigin;

    String tripStatus;
	public SearchResult() {
		
	}
	public String getTripAct() {
		return tripAct;
	}
	public void setTripAct(String tripAct) {
		this.tripAct = tripAct;
	}
	public String getTripBudget() {
		return tripBudget;
	}
	public void setTripBudget(String tripBudget) {
		this.tripBudget = tripBudget;
	}
	public String getTripChks() {
		return tripChks;
	}
	public void setTripChks(String tripChks) {
		this.tripChks = tripChks;
	}
	public String getTripDate() {
		return tripDate;
	}
	public void setTripDate(String tripDate) {
		this.tripDate = tripDate;
	}
	public String getTripDestination() {
		return tripDestination;
	}
	public void setTripDestination(String tripDestination) {
		this.tripDestination = tripDestination;
	}
	public String getTripID() {
		return tripID;
	}
	public void setTripID(String tripID) {
		this.tripID = tripID;
	}
	public String getTripOrigin() {
		return tripOrigin;
	}
	public void setTripOrigin(String tripOrigin) {
		this.tripOrigin = tripOrigin;
	}
	public String getTripStatus() {
		return tripStatus;
	}
	public void setTripStatus(String tripStatus) {
		this.tripStatus = tripStatus;
	}

}
